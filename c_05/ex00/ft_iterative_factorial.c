int	ft_iterative_factorial(int nb)
{
	int i;
	int nbr;

	nbr = 1;
	i = 0;
	if (nb < 0)
		return (0);
	while (i < nb)
	{
		nbr = nbr * (i + 1);
		i++;
	}
	return (nbr);
}
